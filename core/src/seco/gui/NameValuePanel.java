    public String getName()
    {
        return (!name.getText().equals(DEF)) ? name.getText() : "";
    }
    
    public String getValue()
    {
        return (!value.getText().equals(DEF)) ? value.getText() : "";
    }
    
    public void setName(String _name)
    {
        name.setText(_name);
    }
    
    public void setValue(String _name)
    {
        value.setText(_name);
    }
    
    public void disableField(int i)
    {
        if(i==0)
          name.setEnabled(false);
        else if(i==1)
          value.setEnabled(false);
    }
    
    public void enableField(int i)
    {
        name.setEnabled(i==0 || i==1);
    }
    
    public void hideEntry(int i)
    {
        if(i==0)
        {
           name.setVisible(false);
           jLabel1.setVisible(false); 
        }
        else if(i==1)
        {
           value.setVisible(false);
           jLabel3.setVisible(false);
        }
    }
   
